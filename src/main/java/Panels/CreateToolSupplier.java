    private void initializeData() {
        lblSupplierName.setText(supplier.getName());
        AutoCompleteDecorator.decorate(cbxDescription);
        AutoCompleteDecorator.decorate(cbxToolCode);
        tools = ToolService.findByNotInSupplier(supplier);
        DefaultComboBoxModel toolsCodeModel = new DefaultComboBoxModel();
        DefaultComboBoxModel descriptionModel = new DefaultComboBoxModel();
        tools.forEach(tool -> {
            toolsCodeModel.addElement(tool.getCode());
            descriptionModel.addElement(tool.getDescription());
        });
        SwingUtilities.invokeLater(() -> {
            cbxToolCode.setModel(toolsCodeModel);
            cbxDescription.setModel(descriptionModel);
            txtCurrency.setModel(new DefaultComboBoxModel(currencies));
        });
    }

