    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        boolean confirmToSave = false;
        Tool tool = new Tool();
        int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this item to the supplier?", "CONFIRM TOOL SUPPLIED", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(confirmation == 0) {
            if(cbxDescription.getSelectedIndex() == -1 || cbxToolCode.getSelectedIndex() == -1) {
                boolean confirmToSaveMaterial = false;
                if(ToolService.isExisting("code", cbxToolCode.getSelectedItem().toString())) {
                    JOptionPane.showMessageDialog(null, "Tool Code is already existing, please reformat the data", "EXISTING UNIQUE TOOL IDENTIFIER", JOptionPane.ERROR_MESSAGE);
                } else {
                    int saveAnyway = 0;
                    if(ToolService.isExisting("description", cbxDescription.getSelectedItem().toString())) {
                        saveAnyway = JOptionPane.showConfirmDialog(null, "Tool Description is already existing, DO YOU WISH TO SAVE IT ANYWAY?", "CONFIRM SAVE EXISTING DATA", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
                    }
                    if(saveAnyway == 0) {
                        confirmToSaveMaterial = true;
                    }
                }
                if(confirmToSaveMaterial) {
                    tool.setCode(cbxToolCode.getSelectedItem().toString());
                    tool.setDescription(cbxDescription.getSelectedItem().toString());
                    tool.setUnit(txtUnit.getText().trim());
                    if(ToolService.saveTool(tool)) {
                        confirmToSave = true;
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to save tool, please contact technical support.", "SAVING FAILED", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                tool = tools.get(cbxDescription.getSelectedIndex());
                confirmToSave = true;
            }
            if(confirmToSave) {
                toolSupplier = new ToolSupplier();
                toolSupplier.setSupplier(supplier);
                toolSupplier.setTool(tool);
                toolSupplier.setCurrency(txtCurrency.getSelectedItem().toString());
                toolSupplier.setPrice(BigDecimal.valueOf(Double.parseDouble(txtPrice.getText())));
                if(ToolSupplierService.saveToolSupplier(toolSupplier)) {
                    JOptionPane.showMessageDialog(null, "Tool SUPPLIER SUCCESSFULLY SAVED!!");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to save Material Supplier, Please Contact Technical Support", "TRANSACTION FAILED", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


